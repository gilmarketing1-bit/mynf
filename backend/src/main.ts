  app.setGlobalPrefix('api');

  const port = process.env.PORT || 3001;
  await app.listen(port);
  console.log(`ğŸš€ myNF Backend rodando na porta ${port}`);
  console.log(`ğŸ›¡ï¸  Helmet, CORS e Rate Limiting ativos`);
